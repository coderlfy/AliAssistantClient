package com.cat.model;

public class KeyValue {
	private String _key;
    public String getKey() {
        return _key;
    }
    public void setKey(String key) {
        this._key = key;
    }

	private Object _value;
    public Object getValue() {
        return _value;
    }
    public void setValue(Object value) {
        this._value = value;
    }

}
